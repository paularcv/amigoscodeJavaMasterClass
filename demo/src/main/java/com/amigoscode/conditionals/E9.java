package com.amigoscode.conditionals;

public class E9 {
    public static void main(String[] args) {
        // create a for loop which goes from 0 to 20
        // print each even value

        for (int i = 0; i <= 20; i ++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
